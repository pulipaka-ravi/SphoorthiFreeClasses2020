# ask the user to enter anyone from 1-5 numbers
# and convert the number into words

number = int(input("Please enter any number from 1-5: "))

if number==1:
    print("you've entered One")
elif number==2:
    print("you've entered two")
elif number==3:
    print("you've entered three")
elif number==4:
    print("you've entered four")
elif number==5:
    print("you've entered  five")
else:
    print("The number you've used is out of range")
    
